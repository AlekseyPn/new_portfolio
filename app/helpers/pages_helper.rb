module PagesHelper
  URL_REGEX = %r{
      \b
      (
        (?: [a-z][\w-]+:
         (?: /{1,3} | [a-z0-9%] ) |
          www\d{0,3}[.] |
          [a-z0-9.\-]+[.][a-z]{2,4}/
        )
        (?:
         [^\s()<>]+ | \(([^\s()<>]+|(\([^\s()<>]+\)))*\)
        )+
        (?:
          \(([^\s()<>]+|(\([^\s()<>]+\)))*\) |
          [^\s`!()\[\]{};:'".,<>?«»“”‘’]
        )
      )
    }ix
  def twitter_parser tweet
    tweet.gsub(URL_REGEX) do |url|
      "<a href='#{url}' target='_blank' class='tweet-link'>#{url}</a>"
    end.html_safe
  end
  
end
